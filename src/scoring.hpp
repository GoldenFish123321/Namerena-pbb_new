// ===== hanxu_Poly: 多项式特征扩展 (预计算表版) =====
// 用编译期常量 POLY_TABLE 替代运行时反向追踪。
// 1034 次查表 + 直接计算, 消除 ~45000 次循环迭代。
inline void hanxu_Poly(double *xp, const double *x) {
  for (int y = 0; y < 1034; y++) {
    auto& e = POLY_TABLE[y];
    xp[y] = e.is_pair ? x[e.p] * x[e.p + e.q] : x[e.q];
  }
}

